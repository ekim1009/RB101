# the difference between ! and ?
# ! means not equals if it is in front of something
# ? means question

what is != and where should you use it? 
# != means not equal to.  You should use it when you are trying to get the opposite of something
put ! before something, like !user_name
# ! before something, you are making it the opposite (not)
put ! after something, like words.uniq!
# ! after something means you want the mutating method
put ? before something
# ? before : is the ternary operator for if..else
put ? after something
# ? after a method name does not mean anything, is just part of the name
put !! before something, like !!user_name
# !! before anything turns the object into their boolean equivalent.